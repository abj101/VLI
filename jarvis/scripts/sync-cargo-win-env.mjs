#!/usr/bin/env node

/**
 * Writes `src-tauri/.cargo/config.local.toml` so bare `cargo check` / rust-analyzer get the same
 * whisper-rs-sys env as `scripts/whisper-gpu/run-tauri.mjs`.
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

import {
  buildWindowsWhisperCargoEnv,
  tomlEnvValue,
  validateWindowsWhisperBindgenEnv,
} from "./whisper-gpu/win-env.mjs";

const JARVIS_ROOT = path.join(path.dirname(fileURLToPath(import.meta.url)), "..");
const CARGO_DIR = path.join(JARVIS_ROOT, "src-tauri", ".cargo");
const WINDOWS_TARGET = "x86_64-pc-windows-msvc";

function writeRustAnalyzerToml(discovered) {
  const bindgen = discovered.BINDGEN_EXTRA_CLANG_ARGS;
  const libClang = discovered.LIBCLANG_PATH;
  if (!bindgen && !libClang) return;

  /** @type {Record<string, string>} */
  const extraEnv = {};
  if (libClang) extraEnv.LIBCLANG_PATH = libClang;
  if (bindgen) {
    extraEnv.BINDGEN_EXTRA_CLANG_ARGS = bindgen;
    extraEnv.BINDGEN_EXTRA_CLANG_ARGS_x86_64_pc_windows_msvc = bindgen;
    extraEnv["BINDGEN_EXTRA_CLANG_ARGS_x86_64-pc-windows-msvc"] = bindgen;
  }

  const lines = [
    "# Generated by scripts/sync-cargo-win-env.mjs — do not edit.",
    "# rust-analyzer does not always apply .cargo/config.local.toml [env] to build scripts.",
    "[cargo]",
    "extraEnv = {",
  ];
  for (const [key, value] of Object.entries(extraEnv)) {
    const keyToml = key.includes("-") ? `"${key}"` : key;
    lines.push(`  ${keyToml} = ${tomlEnvValue(value)},`);
  }
  lines.push("}", "");

  const out = path.join(JARVIS_ROOT, "rust-analyzer.toml");
  fs.writeFileSync(out, lines.join("\n"), "utf8");
  console.log(`sync-cargo-win-env: wrote ${path.relative(JARVIS_ROOT, out)}`);
}

function buildConfigTomlBody(discovered) {
  const lines = [
    "# Generated by scripts/sync-cargo-win-env.mjs — do not edit.",
    "# Lets bare `cargo check` / rust-analyzer build whisper-rs-sys without npm run tauri.",
    "# E0080 (usize overflow in bindings.rs) = bindgen failed; stale Linux fallback bindings.",
    "# Does NOT set CMAKE_GENERATOR — avoids invalidating whisper-cuda (NMake) CMake cache used by",
    "# `npm run tauri dev`. For whisper-cuda checks use: npm run test:cargo-whisper-cuda",
    "[env]",
  ];

  const forceKeys = new Set([
    "LIBCLANG_PATH",
    "BINDGEN_EXTRA_CLANG_ARGS",
    "BINDGEN_EXTRA_CLANG_ARGS_x86_64_pc_windows_msvc",
    "BINDGEN_EXTRA_CLANG_ARGS_x86_64-pc-windows-msvc",
  ]);

  for (const [key, value] of Object.entries(discovered)) {
    const tomlVal = tomlEnvValue(value);
    if (forceKeys.has(key)) {
      lines.push(`${key} = { value = ${tomlVal}, force = true }`);
    } else {
      lines.push(`${key} = ${tomlVal}`);
    }
  }

  const bindgen = discovered.BINDGEN_EXTRA_CLANG_ARGS;
  if (bindgen) {
    lines.push("");
    lines.push(`[target.${WINDOWS_TARGET}.env]`);
    lines.push(
      `BINDGEN_EXTRA_CLANG_ARGS = { value = ${tomlEnvValue(bindgen)}, force = true }`,
    );
    lines.push(
      `BINDGEN_EXTRA_CLANG_ARGS_x86_64_pc_windows_msvc = { value = ${tomlEnvValue(bindgen)}, force = true }`,
    );
    lines.push(
      `BINDGEN_EXTRA_CLANG_ARGS_x86_64-pc-windows-msvc = { value = ${tomlEnvValue(bindgen)}, force = true }`,
    );
  }

  if (Object.keys(discovered).length === 0) {
    lines.push("# (no paths discovered — install VS 2022 Build Tools + LLVM, then re-run)");
  }

  return `${lines.join("\n")}\n`;
}

function main() {
  const localToml = path.join(CARGO_DIR, "config.local.toml");
  const legacyRootCargo = path.join(JARVIS_ROOT, ".cargo", "config.local.toml");

  if (process.platform !== "win32") {
    for (const out of [localToml, legacyRootCargo]) {
      if (fs.existsSync(out)) {
        fs.unlinkSync(out);
        console.log(`sync-cargo-win-env: removed ${path.relative(JARVIS_ROOT, out)}`);
      }
    }
    const ra = path.join(JARVIS_ROOT, "rust-analyzer.toml");
    if (fs.existsSync(ra)) {
      fs.unlinkSync(ra);
      console.log(`sync-cargo-win-env: removed ${path.relative(JARVIS_ROOT, ra)}`);
    }
    return;
  }

  const check = validateWindowsWhisperBindgenEnv();
  if (!check.ok) {
    console.warn("sync-cargo-win-env: whisper-rs-sys bindgen prerequisites missing:");
    for (const e of check.errors) console.warn(`  - ${e}`);
    for (const w of check.warnings) console.warn(`  ${w}`);
  }

  const discovered = buildWindowsWhisperCargoEnv({}, {
    force: true,
    includeCmakeGenerator: false,
  });
  const body = buildConfigTomlBody(discovered);

  fs.mkdirSync(CARGO_DIR, { recursive: true });
  fs.writeFileSync(localToml, body, "utf8");
  console.log(
    `sync-cargo-win-env: wrote ${path.relative(JARVIS_ROOT, localToml)} (${Object.keys(discovered).length} vars)`,
  );

  if (fs.existsSync(legacyRootCargo)) {
    fs.unlinkSync(legacyRootCargo);
    console.log(
      `sync-cargo-win-env: removed legacy ${path.relative(JARVIS_ROOT, legacyRootCargo)} (use src-tauri/.cargo only)`,
    );
  }

  writeRustAnalyzerToml(discovered);

  if (check.ok && discovered.BINDGEN_EXTRA_CLANG_ARGS) {
    console.log("sync-cargo-win-env: bindgen env OK (MSVC + Windows SDK includes for whisper-rs-sys)");
  }
}

main();
